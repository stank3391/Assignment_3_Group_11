package A3G11;

/**
 * @brief Enum representing the possible positions of a door.
 *
 * This enum defines the two primary states a door can be in, either OPEN or CLOSED, used to indicate the current status of a door object.
 */
public enum Position {
	OPEN,  /**< Indicates that the door is open. */
	CLOSED /**< Indicates that the door is closed. */
}
