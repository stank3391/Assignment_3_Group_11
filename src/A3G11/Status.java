package A3G11;


/**
 * @brief Enum representing the operational status of a device.
 *
 * This enum defines the possible operational states of a device, indicating whether the device is currently turned on or off. It is used within the device management system to track and control device status.
 */
public enum Status {
	ON, /**< Indicates that the device is turned on  */
	OFF /**< Indicates that the device is turned off  */
}
